function c = make_colors(n)
hsv= [  1, .5,  0;...
        1,  1,  0;...
       .5,  1,  0;...
        0,  1,  0;...
        0,  1, .5;...
        0,  1,  1;...
        0, .5,  1;...
        0,  0,  1;...
       .5,  0,  1;...
        1,  0,  1;...a
        1,  0, .5;...
        1,  0,  0;...
        ];
x  = [1:12]';
xq = linspace(1,12,n)';
c = [interp1(x,hsv(:,1),xq), interp1(x,hsv(:,2),xq), interp1(x,hsv(:,3),xq)];